findPath()

const tileSize = 20
canvas.width = graph.grid[0].length * tileSize
canvas.height = graph.grid.length * tileSize

function drawGrid () {
	const tilesX = canvas.width / tileSize
	const tilesY = canvas.height / tileSize
	ctx.strokeStyle = 'gray'

	ctx.beginPath()
	
	for (let x = 0; x < tilesX; x++) {
		ctx.moveTo(x * tileSize, 0)
		ctx.lineTo(x * tileSize, canvas.height)
	}

	for (let y = 0; y < tilesY; y++) {
		ctx.moveTo(0, y * tileSize)
		ctx.lineTo(canvas.width, y * tileSize)
	}
	
	ctx.closePath()
	ctx.stroke()
}

function drawTiles () {
	for (var y = 0; y < graph.grid.length; y++) {
		for (var x = 0; x < graph.grid[y].length; x++) {
			const node = graph.grid[y][x]
			if (node.weight === 0) {
				ctx.fillStyle = 'rgba(0, 0, 0, 1)'
				ctx.fillRect(x * tileSize, y * tileSize, tileSize, tileSize)
			}
		}
	}
}

function drawPath () {
	ctx.strokeStyle = 'red'
	ctx.beginPath()

	const half = tileSize / 2

	for (var i = 0; i < path.length; i++) {
		const node = path[i]
		ctx.moveTo(node.y * tileSize + half, node.x * tileSize + half)
		ctx.lineTo(node.parent.y * tileSize + half, node.parent.x * tileSize + half)
	}

	ctx.closePath()
	ctx.stroke()
}

const img = document.querySelector('img')

function draw () {
	requestAnimationFrame(draw)
	ctx.clearRect(0, 0, canvas.width, canvas.height)
	// ctx.drawImage(img, 0, 0, canvas.width, canvas.height)
	if (drawing) {
		drawGrid()
	}
	drawTiles()
	drawPath()
}

draw()

let setStart = false

canvas.onmousedown = (event) => {
	let x = Math.floor((event.clientX - canvas.offsetLeft + window.pageXOffset) / tileSize)
	let y = Math.floor((event.clientY - canvas.offsetTop + window.pageYOffset) / tileSize)

	// event.button: 0 = left, 1 = middle, 2 = right

	if (event.button === 0) {
		if (setStart) {
			end = {x, y}
			setStart = false
			findPath()
		} else {
			start = {x, y}
			setStart = true
		}
	} else {
		if (nodes[y][x] === 1) {
			nodes[y][x] = 0
		} else {
			nodes[y][x] = 1
		}
		buildGraph()
		draw()
		localStorage.setItem('map', JSON.stringify(nodes))
	}
}

canvas.oncontextmenu = () => {
	return false
}

document.onkeydown = (event) => {
	if (event.key === 'd') {
		drawing = !drawing
	}
}
