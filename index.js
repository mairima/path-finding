function buildGraph () {
	graph = new Graph(nodes, {diagonal: true})
}

function findPath () {

	const graphStart = graph.grid[start.y][start.x]

	const graphEnd = graph.grid[end.y][end.x]

	path = astar.search(graph, graphStart, graphEnd)

}

const dataLoaded = localStorage.getItem('map')
if (dataLoaded) nodes = JSON.parse(dataLoaded)
